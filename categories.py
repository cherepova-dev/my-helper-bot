def _normalize(text: str) -> str:
    """Приведение текста к виду для поиска: нижний регистр, схлопывание пробелов."""
    if not text:
        return ""
    return re.sub(r"\s+", " ", text.strip().lower())


def assign_category(task_text: str) -> tuple[str, str]:
    """
    Определяет категорию задачи по тексту.
    Возвращает (emoji, name).
    """
    text = _normalize(task_text)
    if not text:
        return "📝", "Другое"

    best_emoji = "📝"
    best_name = "Другое"
    best_score = 0

    for cat_id, emoji, name, keywords in CATEGORIES:
        if cat_id == "other":
            continue
        score = 0
        for kw in keywords:
            if kw in text:
                score += 1
        if score > best_score:
            best_score = score
            best_emoji = emoji
            best_name = name

    return best_emoji, best_name
